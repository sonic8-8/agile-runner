CREATE TABLE IF NOT EXISTS TASK_RUNTIME_STATE (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_key VARCHAR(255) NOT NULL,
    issue_number BIGINT,
    title VARCHAR(255) NOT NULL,
    status VARCHAR(32) NOT NULL,
    retry_count INT NOT NULL DEFAULT 0,
    owner_role VARCHAR(32),
    started_at TIMESTAMP,
    finished_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_task_runtime_state_task_key UNIQUE (task_key)
);

CREATE INDEX IF NOT EXISTS idx_task_runtime_state_issue_number ON TASK_RUNTIME_STATE (issue_number);

CREATE TABLE IF NOT EXISTS VALIDATION_CRITERIA (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_key VARCHAR(255) NOT NULL,
    criteria_key VARCHAR(100) NOT NULL,
    category VARCHAR(32) NOT NULL,
    description CLOB NOT NULL,
    status VARCHAR(32) NOT NULL,
    evidence CLOB,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_validation_criteria_task_key UNIQUE (task_key, criteria_key)
);

CREATE INDEX IF NOT EXISTS idx_validation_criteria_task_key ON VALIDATION_CRITERIA (task_key);

CREATE TABLE IF NOT EXISTS WEBHOOK_EXECUTION (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    execution_key VARCHAR(255) NOT NULL,
    task_key VARCHAR(255) NOT NULL,
    delivery_id VARCHAR(255) NOT NULL,
    retry_source_execution_key VARCHAR(255),
    repository_name VARCHAR(255) NOT NULL,
    pull_request_number BIGINT NOT NULL,
    event_type VARCHAR(64) NOT NULL,
    action VARCHAR(64),
    status VARCHAR(32) NOT NULL,
    error_message CLOB,
    error_code VARCHAR(128),
    failure_disposition VARCHAR(64),
    execution_start_type VARCHAR(32),
    execution_control_mode VARCHAR(32),
    write_performed BOOLEAN,
    write_skip_reason VARCHAR(64),
    selection_applied BOOLEAN,
    selected_paths_summary CLOB,
    started_at TIMESTAMP NOT NULL,
    finished_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_webhook_execution_execution_key UNIQUE (execution_key),
    CONSTRAINT uq_webhook_execution_delivery_id UNIQUE (delivery_id)
);

CREATE INDEX IF NOT EXISTS idx_webhook_execution_task_key ON WEBHOOK_EXECUTION (task_key);
CREATE INDEX IF NOT EXISTS idx_webhook_execution_pull_request_number ON WEBHOOK_EXECUTION (pull_request_number);

